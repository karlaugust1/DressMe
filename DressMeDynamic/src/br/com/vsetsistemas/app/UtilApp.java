package br.com.vsetsistemas.app;

public class UtilApp {

	public int clientesRegistrados;
	public int pedidosVendas;
	public int pedidosVendasMes;
	public double totalPedidos;
	
	public UtilApp() {
		
	}

	public int getClientesRegistrados() {
		return clientesRegistrados;
	}

	public void setClientesRegistrados(int clientesRegistrados) {
		this.clientesRegistrados = clientesRegistrados;
	}

	public int getPedidosVendas() {
		return pedidosVendas;
	}

	public void setPedidosVendas(int pedidosVendas) {
		this.pedidosVendas = pedidosVendas;
	}

	public int getPedidosVendasMes() {
		return pedidosVendasMes;
	}

	public void setPedidosVendasMes(int pedidosVendasMes) {
		this.pedidosVendasMes = pedidosVendasMes;
	}

	public double getTotalPedidos() {
		return totalPedidos;
	}

	public void setTotalPedidos(double totalPedidos) {
		this.totalPedidos = totalPedidos;
	}
	
	
}
